package edu.cmu.cs.cs214.rec02;

/**
 * IntQueue интерфэйс нь FIFO (First-In, First-Out) бүтэцтэй
 * бүхэл тоон дугаарласан дарааллыг тодорхойлно.
 *
 * Доорх тайлбаруудыг Монгол хэл дээр илүү ойлгомжтой болгох,
 * болон лабораторийн тест бичихэд илүү тодорхой болгохын тулд ЗАСВАР хийсэн.
 *
 *  АНХААРАХ НЬ:
 * Интерфэйсийн нэг ч method-ийн signature-г өөрчлөөгүй.
 * Зөвхөн тодорхойлолтыг ойлгомжтой болгох зорилгоор тайлбар нэмсэн.
 */
public interface IntQueue {

    /**
     * Queue доторх бүх элементүүдийг устгана.
     * Энэ үйлдлийн дараа:
     *  - size() == 0 байх ёстой
     *  - isEmpty() == true байх ёстой
     *  - peek() == null буцаах ёстой
     *  - dequeue() == null буцаах ёстой
     *
     *  ЯАГААД ЗАСАВ?
     * Эх тайлбар нь clear() дараа ямар төлөв үүсэхийг
     * тодорхой бичээгүй. Тест бичих үед шаардлага маш тодорхой байх ёстой.
     */
    void clear();

    /**
     * Queue-ийн HEAD дээрх элементийг авч, устгаж буцаана.
     *
     * @return
     *   - Queue хоосон бол → null
     *   - Үгүй бол → FIFO зарчмаар хамгийн эхэнд орсон элемент
     *
     *  ЯАГААД ЗАСАВ?
     * Эх тайлбар хангалттай сайн байсан ч,
     * лабораторийн тестүүд дээр хоосон үед NULL буцаахыг
     * онцгойлон тодруулдаг. Тиймээс тайлбар илүү тодорхой болгов.
     */
    Integer dequeue();

    /**
     * Queue-ийн TAIL хэсэгт шинэ элемент нэмнэ.
     *
     * @param value — нэмж буй элемент (Integer object)
     * @return үргэлж true буцаана.
     *
     *  ЯАГААД ЗАСАВ?
     * Эх код "successfully" гэж бичсэн ч,
     * манай лабораторийн queue implementation-д
     * enqueue() хэзээ ч false буцаахгүй.
     * Энэ нь тест бичигчдэд анхны ойлголтыг өгнө.
     */
    boolean enqueue(Integer value);

    /**
     * Queue хоосон эсэхийг шалгана.
     *
     * @return
     *   - size() == 0 → true
     *   - size() > 0 → false
     *
     *  ЯАГААД ЗАСАВ?
     * ArrayIntQueue болон LinkedIntQueue implementation дээр
     * isEmpty() зөв ажиллах ёстойг тест бичихэд чухал.
     */
    boolean isEmpty();

    /**
     * Queue-ийн HEAD дээрх элементийг УСТГАХГҮЙгээр авч харах.
     *
     * @return
     *   - Queue хоосон бол → null
     *   - Үгүй бол → dequeue() буцаах байсан эхний элемент
     *
     *  ЯАГААД ЗАСАВ?
     * ArrayIntQueue анхны хувилбарт peek() буруу байсан (null биш 0 буцаадаг байсан).
     * Тиймээс interface дээр энэ нөхцөлийг илүү тодруулж өглөө.
     */
    Integer peek();

    /**
     * Queue доторх элементүүдийн тоог буцаана.
     *
     * @return queue-ийн одоогийн хэмжээ
     *
     *  ЯАГААД ЗАСАВ?
     * Энэ method тест бичихэд ашиглагдах тул,
     * "ямар үед size өөрчлөгдөх" зэргийг ойлгоход туслах тайлбар нэмсэн.
     */
    int size();
}
